public  void populateJTable(){
      myquery3 mq=new myquery3();
      ArrayList<User2> list=mq.BindTable();
    String [] columnName={"Book_ID","Name","Edition","Author","Price","Pages","Image_B"};
    Object[][]rows=new Object[list.size()][7];
    for(int i=0;i<list.size();i++)
    {
        rows[i][0]=list.get(i).getid();
        rows[i][1]=list.get(i).getBName();
        
        rows[i][2]=list.get(i).getEdition();
        rows[i][3]=list.get(i).getAuthor();
      rows[i][4]=list.get(i).getPrice();
       rows[i][5]=list.get(i).getPages();
        if(list.get(i).getImage()!=null){
            
            ImageIcon image=new ImageIcon( new ImageIcon(list.get(i).getImage()).getImage().getScaledInstance(120,120,Image.SCALE_SMOOTH));
        rows[i][6]=image;
        }
            else{
                    rows[i][6]=null;
        }
   
    TheMode3 mode1 =new TheMode3(rows,columnName);
    jTable2.setModel(mode1);
   jTable2.setRowHeight(120);
   jTable2.getColumnModel().getColumn(6).setPreferredWidth(100);
    
    }}
