    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String path="";
        JFileChooser j=new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x= j.showSaveDialog(this);
        if(x==JFileChooser.APPROVE_OPTION){
            path=j.getSelectedFile().getPath();
        }
        Document doc=new Document();
        try {
            PdfWriter writer=PdfWriter.getInstance(doc,new FileOutputStream(path+"Book Details.pdf"));
            doc.open();
            PdfPTable tb1=new PdfPTable(7);
            tb1.addCell("Book_Id");
            tb1.addCell("Name");
            tb1.addCell("Eduition");
tb1.addCell("Author");
tb1.addCell("Price");
            tb1.addCell("Pages");

            for(int i=0;i<jTable2.getRowCount();i++){
                String Book_Id=jTable2.getValueAt(i, 0).toString();
                String Name=jTable2.getValueAt(i, 1).toString();
                String Edition=jTable2.getValueAt(i, 2).toString();
                String Author=jTable2.getValueAt(i, 3).toString();
String Price=jTable2.getValueAt(i, 4).toString();
String Pages=jTable2.getValueAt(i, 5).toString();
                tb1.addCell(Book_Id);
                tb1.addCell(Name);

                tb1.addCell(Edition);
                tb1.addCell(Author);
                 tb1.addCell(Price);
                  tb1.addCell(Pages);

            }

            doc.add(tb1);

        } catch (DocumentException e) {
            e.printStackTrace();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        }
        doc.close();
    }//GEN-LAST:event_jButton3ActionPerformed

